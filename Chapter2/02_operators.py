# assigment operators
x = 10
y = 5
x += y  # x = x + y
print("After += :", x)  # Output: 15
x -= y  # x = x - y
print("After -= :", x)  # Output: 10
x *= y  # x = x * y
print("After *= :", x)  # Output: 50

# comparison operators
a = 10
b = 20
print("a == b :", a == b)  # Output: False
print("a != b :", a != b)  # Output: True
print("a > b  :", a > b)   # Output: False
print("a < b  :", a < b)   # Output: True
print("a >= b :", a >= b)  # Output: False
print("a <= b :", a <= b)  # Output: True